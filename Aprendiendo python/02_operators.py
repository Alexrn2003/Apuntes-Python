### Operadores Aritméticos###

print(3 + 4) #suma
print(3 - 4) #resta
print(3 * 4) #multiplicación
print(3 / 4) #division
print(10 % 4) #modulo
print(10 // 3) #floor division(se acerca/aproxima a un numero entero)
print(2 ** 3) #calculo de un exponente(ej:2^3=8)
print(2 ** 3 + 4 / 6 // 3 )

print("Hola " + "Python") #podemos contenar palabras 
print("Hola " + str(5)) #concatenamos también con números
print("Hola" * 5)

my_float = 2.5 * 2
print("Hola " * int(my_float))

### Operadores Comparativos###

print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)
print(3 < 4)
print(3 == 4)


